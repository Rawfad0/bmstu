def xtime(a: int) -> int:
    """умножение в GF(2^8) на x (0x02)"""
    return (((a << 1) & 0xFF) ^ (0x1B if (a & 0x80) else 0x00)) & 0xFF


def mul(a: int, b: int) -> int:
    """умножение в GF(2^8) (наивный битовый метод)"""
    res = 0
    for i in range(8):
        if b & 1:
            res ^= a
        hi = a & 0x80
        a = (a << 1) & 0xFF
        if hi:
            a ^= 0x1B
        b >>= 1
    return res


def keyExpansion(key: list[int]) -> np.ndarray:
    if len(key) < 4 * N_K:
        raise ValueError("key length too small")
    ks = np.zeros((4, N_B * (N_R + 1)), dtype=np.uint8)
    k = np.array(key[:4 * N_K], dtype=np.uint8).reshape(4, N_K, order='F')
    ks[:, :N_K] = k
    for col in range(N_K, N_B * (N_R + 1)):
        temp = ks[:, col - 1].copy()
        if col % N_K == 0:
            temp = np.roll(temp, -1)         # RotWord
            temp = SBOX[temp]               # SubWord
            temp ^= RCON[:, col // N_K - 1] # Rcon
        ks[:, col] = ks[:, col - N_K] ^ temp
    return ks


def addRoundKey(state: np.ndarray, ks: np.ndarray, rnd: int = 0) -> np.ndarray:
    return (state ^ ks[:, N_B * rnd:N_B * (rnd + 1)])


def subBytes(state: np.ndarray, inv: bool = False) -> np.ndarray:
    box = INV_SBOX if inv else SBOX
    return box[state]


def shiftRows(state: np.ndarray, inv: bool = False) -> np.ndarray:
    out = state.copy()
    for r in range(1, 4):
        out[r] = np.roll(out[r], -r if not inv else r)
    return out


def mixColumns(state: np.ndarray, inv: bool = False) -> np.ndarray:
    out = np.zeros_like(state)
    for c in range(4):
        a = state[:, c].tolist()
        if not inv:
            out[0, c] = (xtime(a[0]) ^ (xtime(a[1]) ^ a[1]) ^ a[2] ^ a[3]) & 0xFF
            out[1, c] = (a[0] ^ xtime(a[1]) ^ (xtime(a[2]) ^ a[2]) ^ a[3]) & 0xFF
            out[2, c] = (a[0] ^ a[1] ^ xtime(a[2]) ^ (xtime(a[3]) ^ a[3])) & 0xFF
            out[3, c] = ((xtime(a[0]) ^ a[0]) ^ a[1] ^ a[2] ^ xtime(a[3])) & 0xFF
        else:
            out[0, c] = (mul(a[0], 0x0e) ^ mul(a[1], 0x0b) ^ mul(a[2], 0x0d) ^ mul(a[3], 0x09)) & 0xFF
            out[1, c] = (mul(a[0], 0x09) ^ mul(a[1], 0x0e) ^ mul(a[2], 0x0b) ^ mul(a[3], 0x0d)) & 0xFF
            out[2, c] = (mul(a[0], 0x0d) ^ mul(a[1], 0x09) ^ mul(a[2], 0x0e) ^ mul(a[3], 0x0b)) & 0xFF
            out[3, c] = (mul(a[0], 0x0b) ^ mul(a[1], 0x0d) ^ mul(a[2], 0x09) ^ mul(a[3], 0x0e)) & 0xFF
    return out.astype(np.uint8)


def encrypt(input_bytes: list[int], key: list[int]) -> list[int]:
    state = np.array(input_bytes, dtype=np.uint8).reshape(4, 4, order='F')
    ks = keyExpansion(key)
    state = addRoundKey(state, ks, 0)
    for rnd in range(1, N_R):
        state = subBytes(state, inv=False)
        state = shiftRows(state, inv=False)
        state = mixColumns(state, inv=False)
        state = addRoundKey(state, ks, rnd)
    # final round (no mixColumns)
    state = subBytes(state, inv=False)
    state = shiftRows(state, inv=False)
    state = addRoundKey(state, ks, N_R)
    return state.reshape(16, order='F').tolist()


def decrypt(cipher: list[int], key: list[int]) -> list[int]:
    state = np.array(cipher, dtype=np.uint8).reshape(4, 4, order='F')
    ks = keyExpansion(key)
    state = addRoundKey(state, ks, N_R)
    for rnd in range(N_R - 1, 0, -1):
        state = shiftRows(state, inv=True)
        state = subBytes(state, inv=True)
        state = addRoundKey(state, ks, rnd)
        state = mixColumns(state, inv=True)
    state = shiftRows(state, inv=True)
    state = subBytes(state, inv=True)
    state = addRoundKey(state, ks, 0)
    return state.reshape(16, order='F').tolist()


def textToBlock(text: str) -> list[int]:
    data = [ord(c) for c in text]
    pad_len = 16 - (len(data) % 16)
    if pad_len == 0:
        pad_len = 16
    data += [pad_len] * pad_len
    return data[:16]  # здесь мы работаем с одним 16-байтовым блоком


def blockToText(block: list[int]) -> str:
    if not block:
        return ""
    pad_len = block[-1]
    if pad_len < 1 or pad_len > 16:
        pad_len = 0
    return "".join(chr(b) for b in block[:-pad_len]) if pad_len else "".join(chr(b) for b in block)


def blockToBase64(block: list[int]) -> str:
    return base64.b64encode(bytes(block)).decode('utf-8')


def base64ToBlock(b64_str: str) -> list[int]:
    return list(base64.b64decode(b64_str))


