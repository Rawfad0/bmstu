/*!
 * \brief ะะตะบัะพัะฝะพะต ะฟัะพะธะทะฒะตะดะตะฝะธะต
 *
 * \param vector_a_x ะบะพะพัะดะธะฝัะฐ x ะฒะตะบัะพัะฐ a
 * \param vector_a_y ะบะพะพัะดะธะฝัะฐ y ะฒะตะบัะพัะฐ a
 * \param vector_b_x ะบะพะพัะดะธะฝัะฐ x ะฒะตะบัะพัะฐ b
 * \param vector_b_y ะบะพะพัะดะธะฝัะฐ y ะฒะตะบัะพัะฐ b
 * \return ะฒะตะบัะพัะฝะพะต ะฟัะพะธะทะฒะตะดะตะฝะธะต ะฒะตะบัะพัะพะฒ a ะธ b
 *
 * ะะฐะฝะฝะฐั ััะฝะบัะธั ะฟัะธะฝะธะผะฐะตั ะบะพะพัะดะธะฝะฐัั x, y ะฒะตะบัะพัะพะฒ a ะธ b,
 * ะบะพัะพััะต ัะฒะปััััั ะดัะพะฑะฝัะผะธ ัะธัะปะฐะผะธ vector_a_x, vector_a_y, vector_b_x, vector_b_y,
 * ะธ ะฒะพะทะฒัะฐัะฐะตั ะธั ะฒะตะบัะพัะฝะพะต ะฟัะพะธะทะฒะตะดะตะฝะธะต, ะบะพัะพัะพะต ัะฒะปัะตััั ะดัะพะฑะฝัะผ ัะธัะปะพะผ.
 */
double vector_product(double vector_a_x, double vector_a_y, double vector_b_x, double vector_b_y)
{
    return vector_a_x * vector_b_y - vector_a_y * vector_b_x;
}

