#include <stdio.h>
#include <stdint.h>

#define FOUR_BYTES 32               // –ø–æ–¥—Ä–∞–∑—É–º–µ–≤–∞–µ—Ç—Å—è, —á—Ç–æ –≤ –±–∞–π—Ç–µ 8 –±–∏—Ç, –∞ –≤ 4 –±–∞–π—Ç–∞—Ö 32 –±–∏—Ç–∞

#define EXIT_CODE_INCORRECT_INPUT 1 // –æ—à–∏–±–∫–∞ –≤–≤–æ–¥–∞
#define EXIT_CODE_NEGATIVE_N 2      // n < 0

/*!
 * \brief –í—ã–≤–æ–¥–∏—Ç —á–∏—Å–ª–æ –≤ –¥–≤–æ–∏—á–Ω–æ–º –≤–∏–¥–µ
 *
 * \param a –±–µ–∑–∑–Ω–∞–∫–æ–≤–æ–µ —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ –¥–ª–∏–Ω–Ω–æ–π 4 –±–∞–π—Ç–∞
 *
 * –î–∞–Ω–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –ø—Ä–∏–Ω–∏–º–∞–µ—Ç —á–∏—Å–ª–æ, –∫–æ—Ç–æ—Ä–æ–µ —è–≤–ª—è–µ—Ç—Å—è –Ω–∞—Ç—É—Ä–∞–ª—å–Ω—ã–º —á–∏—Å–ª–æ–º "a", 
 * –∏ –≤—ã–≤–æ–¥–∏—Ç –Ω–∞ —ç–∫—Ä–∞–Ω —ç—Ç–æ —á–∏—Å–ª–æ –≤ –¥–≤–æ–∏—á–Ω–æ–π —Å–∏—Å—Ç–µ–º–µ —Å—á–∏—Å–ª–µ–Ω–∏—è.
 */
void binary_print(uint32_t a)
{
    for (int i = FOUR_BYTES - 1; i >= 0; i--)
    {
        uint32_t b = 1 << i;
        printf("%u", a / b);
        a %= b;
    }
    printf("\n");
}

/*!
 * \brief –¶–∏–∫–ª–∏—á–µ—Å–∫–∏ —Å–¥–≤–∏–≥–∞–µ—Ç —á–∏—Å–ª–æ
 *
 * –î–∞–Ω–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –∑–∞–ø—Ä–∞—à–∏–≤–∞–µ—Ç —á–∏—Å–ª–æ, –∫–æ—Ç–æ—Ä–æ–µ —è–≤–ª—è–µ—Ç—Å—è 32-–±–∏—Ç–Ω—ã–º –±–µ–∑–∑–Ω–∞–∫–æ–≤—ã–º —á–∏—Å–ª–æ–º "a",
 * —Å–æ–≤–µ—Ä—à–∞–µ—Ç —Ü–∏–∫–ª–∏—á–µ—Å–∫–∏–π —Å–¥–≤–∏–≥ —ç—Ç–æ–≥–æ —á–∏—Å–ª–∞ –∏ –ø–µ—Ä–µ–¥–∞–µ—Ç –∑–Ω–∞—á–µ–Ω–∏–µ "a" —Ñ—É–Ω–∫—Ü–∏–∏ binary_print.
 */
int main(void)
{
    uint32_t a; // –¥–æ–ø—É—Å—Ç–∏–º –≤ –±–∞–π—Ç–µ 8 –±–∏—Ç, —Ç–æ–≥–¥–∞ 4 –±–∞–π—Ç–∞ = 32 –±–∏—Ç
    int n;

    printf("–í–≤–µ–¥–∏—Ç–µ –±–µ–∑–∑–Ω–∞–∫–æ–≤–æ–µ —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ –¥–ª–∏–Ω–æ–π —á–µ—Ç—ã—Ä–µ –±–∞–π—Ç–∞ A –∏ —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ N: ");
    if (scanf("%u%d", &a, &n) != 2) 
    {
        printf("Error: incorrect input.\n");
        return EXIT_CODE_INCORRECT_INPUT;
    }

    if (n < 0)
    {
        printf("Error: incorrect input.\n");
        return EXIT_CODE_NEGATIVE_N;
    }

    n %= 32;
    a = (a << n) | (a >> (FOUR_BYTES - n));   // –¶–∏–∫–ª–∏—á–µ—Å–∫–∏–π —Å–¥–≤–∏–≥ 
    printf("Result: ");
    binary_print(a);

    return 0;
}