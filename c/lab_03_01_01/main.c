int main(void)
{
    long int matrix[M_MAX][N_MAX] = { 0 };
    size_t m, n;
    long int arr[M_MAX] = { 0 };
    int r;

    if (r = matrix_input(matrix, &m, &n), r != 0)
        return r;

    array_fill(matrix, m, n, arr);
    array_print(arr, m);
    return 0;
}