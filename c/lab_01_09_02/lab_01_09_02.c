#include <stdio.h>
#include <math.h>

#define MACHINE_EPS 0.00000001          // –∫–æ–Ω—Å—Ç–∞–Ω—Ç–∞ –ø–æ–≥—Ä–µ—à–Ω–æ—Å—Ç–∏ –ø—Ä–∏ —Å—Ä–∞–≤–Ω–µ–Ω–∏–∏ –¥—Ä–æ–±–Ω–æ–≥–æ —á–∏—Å–ª–∞ —Å –Ω—É–ª–µ–º

#define EXIT_CODE_INCORRECT_INPUT 1     // –æ—à–∏–±–∫–∞ –≤–≤–æ–¥–∞
#define EXIT_CODE_INCORRECT_N 2         // –ø—É—Å—Ç–∞—è –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å

/*!
 * \brief –í—ã—á–∏—Å–ª—è–µ—Ç –∫–æ—Ä–µ–Ω—å —Å—É–º–º—ã –∞—Ä–≥—É–º–µ–Ω—Ç–∞ –∏ –µ–≥–æ –Ω–æ–º–µ—Ä–∞
 *
 * \param x —ç–ª–µ–º–µ–Ω—Ç –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏
 * \param n –Ω–æ–º–µ—Ä —ç–ª–µ–º–µ–Ω—Ç–∞
 * \return –∫–æ—Ä–µ–Ω—å —Å—É–º–º—ã —ç–ª–µ–º–µ–Ω—Ç–∞ –∏ –µ–≥–æ –Ω–æ–º–µ—Ä–∞
 *
 * –î–∞–Ω–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –ø—Ä–∏–Ω–∏–º–∞–µ—Ç —ç–ª–µ–º–µ–Ω—Ç –Ω–µ–ø—É—Å—Ç–æ–π –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏, –∫–æ—Ç–æ—Ä—ã–π —è–≤–ª—è–µ—Ç—Å—è –¥—Ä–æ–±–Ω—ã–º —á–∏—Å–ª–æ–º x, 
 * –∏ –Ω–æ–º–µ—Ä, –∫–æ—Ç–æ—Ä—ã–π —è–≤–ª—è–µ—Ç—Å—è –¥—Ä–æ–±–Ω—ã–º —á–∏—Å–ª–æ–º, —ç—Ç–æ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞ –≤ –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏, 
 * –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∫–æ—Ä–µ–Ω—å –∏–∑ –∏—Ö —Å—É–º–º—ã, –∫–æ—Ç–æ—Ä—ã–π —è–≤–ª—è–µ—Ç—Å—è –¥—Ä–æ–±–Ω—ã–º —á–∏—Å–ª–æ–º.
 */
long double next_term(long double x, long int n)
{   
    return sqrtl((long double) x + n);
}

/*!
 * \brief –í—ã—á–∏—Å–ª—è–µ—Ç –∑–Ω–∞—á–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ g(x)
 *
 * –î–∞–Ω–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –∑–∞–ø—Ä–∞—à–∏–≤–∞–µ—Ç –Ω–µ–ø—É—Å—Ç—É—é –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å –Ω–µ–æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω—ã—Ö —á–∏—Å–µ–ª, –∫–æ—Ç–æ—Ä—ã–µ —è–≤–ª—è—é—Ç—Å—è –¥—Ä–æ–±–Ω—ã–º–∏ —á–∏—Å–ª–∞–º–∏ x,
 * –∏ –≤—ã–≤–æ–¥–∏—Ç –∑–Ω–∞—á–µ–Ω–∏–µ g(x), –∫–æ—Ç–æ—Ä–æ–µ —è–≤–ª—è–µ—Ç—Å—è –¥—Ä–æ–±–Ω—ã–º —á–∏—Å–ª–æ–º g.
 */
int main(void)
{
    long double x;
    long double g = 0;
    long int n = 0;
    int r;

    printf("–í–≤–µ–¥–∏—Ç–µ –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å –Ω–µ–æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω—ã—Ö —á–∏—Å–µ–ª: \n");
    while (r = scanf("%Lf", &x), (r == 1) & (x >= -MACHINE_EPS))
    {
        g *= (long double) n / (n + 1);
        n++;
        g += (long double) next_term(x, n) / n;
    }

    if (r != 1)
        return EXIT_CODE_INCORRECT_INPUT;


    if (n < MACHINE_EPS)
        return EXIT_CODE_INCORRECT_N;   // –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å –ø—É—Å—Ç–∞—è

    printf("g(x)= %Lf\n", g);

    return 0;
}